#define N 256 // Number of points in the FFT

// Bit-reversal function for reordering the input data
unsigned int bit_reverse(unsigned int x, unsigned int log2n)
{
    unsigned int n = 0;
    unsigned int i;
    for (i = 0; i < log2n; i++) {
        n <<= 1;
        n |= (x & 1);
        x >>= 1;
    }
    return n;
}

// FFT function
void fftia(float *x, float *y)
{
    // Compute the log2 of N
    unsigned int log2n = log2f(N);

    // Reorder the input data
    unsigned int i;
    for (i = 0; i < N; i++) {
        unsigned int j = bit_reverse(i, log2n);
        if (j > i) {
            float temp = x[i];
            x[i] = x[j];
            x[j] = temp;
            temp = y[i];
            y[i] = y[j];
            y[j] = temp;
        }
    }

    // Compute the FFT
    unsigned int n, mmax, istep;
    float wtemp, wr, wpr, wpi, wi, theta;
    float tempr, tempi;
    mmax = 1;
    while (mmax < N) {
        istep = mmax << 1;
        theta = M_PI / mmax;
        wtemp = sinf(0.5 * theta);
        wpr = -2.0 * wtemp * wtemp;
        wpi = sinf(theta);
        wr = 1.0;
        wi = 0.0;
        for (n = 0; n < mmax; n++) {
            for (i = n; i < N; i += istep) {
                unsigned int j = i + mmax;
                tempr = wr * x[j] - wi * y[j];
                tempi = wr * y[j] + wi * x[j];
                x[j] = x[i] - tempr;
                y[j] = y[i] - tempi;
                x[i] += tempr;
                y[i] += tempi;
            }
            wtemp = wr;
            wr = wr * wpr - wi * wpi + wr;
            wi = wi * wpr + wtemp * wpi + wi;
        }
        mmax = istep;
    }
}


